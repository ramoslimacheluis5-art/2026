public class OperacionesBasicas {
    public static void main(String[] args) {
        int x = 12, y = 4;

        System.out.println("Suma: " + (x + y));
        System.out.println("Resta: " + (x - y));
        System.out.println("Multi: " + (x * y));
        System.out.println("Div: " + (x / y));
    }
}